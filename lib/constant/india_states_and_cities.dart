  // Helper methods
  static List<String> getAllStates() {
    return statesAndCities.keys.toList()..sort();
  }

  static List<String> getCitiesForState(String state) {
    return statesAndCities[state] ?? [];
  }

  static List<String> getAllCities() {
    List<String> allCities = [];
    for (List<String> cities in statesAndCities.values) {
      allCities.addAll(cities);
    }
    return allCities..sort();
  }

  // Search functionality
  static List<String> searchCities(String query) {
    if (query.isEmpty) return [];

    List<String> results = [];
    String lowerQuery = query.toLowerCase();

    for (List<String> cities in statesAndCities.values) {
      for (String city in cities) {
        if (city.toLowerCase().contains(lowerQuery)) {
          results.add(city);
        }
      }
    }

    return results..sort();
  }

  static List<String> searchStates(String query) {
    if (query.isEmpty) return [];

    List<String> results = [];
    String lowerQuery = query.toLowerCase();

    for (String state in statesAndCities.keys) {
      if (state.toLowerCase().contains(lowerQuery)) {
        results.add(state);
      }
    }

    return results..sort();
  }

  // Get state for a city
  static String? getStateForCity(String city) {
    for (String state in statesAndCities.keys) {
      if (statesAndCities[state]!.contains(city)) {
        return state;
      }
    }
    return null;
  }
